package io.github.ma1uta.homeserver.model;

import java.util.List;
import java.util.Objects;

/**
 * User interactive session used to perform user authentication.
 */
public class UserInteractiveSession {

    /**
     * Session id (sid).
     */
    private String sessionId;

    /**
     * Completed stages.
     */
    private List<String> completed;

    public String getSessionId() {
        return sessionId;
    }

    public void setSessionId(String sessionId) {
        this.sessionId = sessionId;
    }

    public List<String> getCompleted() {
        return completed;
    }

    public void setCompleted(List<String> completed) {
        this.completed = completed;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        UserInteractiveSession that = (UserInteractiveSession) o;
        return Objects.equals(sessionId, that.sessionId);
    }

    @Override
    public int hashCode() {
        return Objects.hash(sessionId);
    }
}
